#include <stdio.h>
#include <stdlib.h>

int add(num1, num2){
    return num1 + num2;
}
int sub(num1, num2){
    return num1 - num2;
}
int mul(num1, num2){
    return num1 * num2;
}
int divi(num1, num2){
    return num1 / num2;
}
int and(num1, num2){
    return num1 & num2;
}
int or(num1, num2){
    return num1 | num2;
}
int xor(num1, num2){
    return num1 ^ num2;
}
int lshift(num1, num2){
    return num1 << num2;
}
int rshift(num1, num2){
    return num1 >> num2;
}